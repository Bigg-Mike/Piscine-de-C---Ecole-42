void	print_bits(unsigned char octet)
{
	int		i;

	i = 0x80;
	while (i)
	{
		if (octet & i)
		{
			write(1, "1", 1);
		}
		else
		{
			write(1, "0", 1);
		}
		i = i >> 1;
	}
}
